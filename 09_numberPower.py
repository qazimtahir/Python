"""
Calculate Powers of Numbers.

Task: Given two integers base and exponent
Compute base raised to the power of exponent.
base:int = 3
exponent:int = 4
"""

# Given values
base = 3
exponent = 4

# Method 1: Using the ** operator
result = base ** exponent

# Method 2: Using the pow() function (uncomment the following line if you want to use this method)
# result = pow(base, exponent)

# Print the result
print(f"{base} raised to the power of {exponent} is: {result}")
